    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        String name = txtmanuname.getText();
        if(name.trim().equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter a Manufacturer name!");
            return;
        }
        
        for(Manufacturer man: business.getManufacturerDirectory().getManufacturerDirectory())
        {
            if(man.getName().equalsIgnoreCase(name))
            {
                JOptionPane.showMessageDialog(null, "Manufacturer exists!");
            return;
            }
        }
        
        
        business.getManufacturerDirectory().addNewManufacturer(name);
        
        populateTable();
        
        
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
        // TODO add your handling code here:
        
        int row = tbldetails.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a Manufacturer from table");
            return;
        }
        
        Manufacturer manufacturer =(Manufacturer) tbldetails.getValueAt(row, 0);
        business.getManufacturerDirectory().removeManufacturer(manufacturer);
        populateTable();
        
        
    }//GEN-LAST:event_btnremoveActionPerformed

    private void btncreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateActionPerformed
        // TODO add your handling code here:
        int row = tbldetails.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a Manufacturer from table");
            return;
        }
        
        Manufacturer manufacturer =(Manufacturer) tbldetails.getValueAt(row, 0);
        
        ManageManufactureAdminUserAccountJPanel panel = new ManageManufactureAdminUserAccountJPanel(workContainer, manufacturer, business );
        workContainer.add("ManageManufactureAdminUserAccountJPanel", panel);
        CardLayout layout = (CardLayout)workContainer.getLayout();
        layout.next(workContainer);
        
        
    }//GEN-LAST:event_btncreateActionPerformed


